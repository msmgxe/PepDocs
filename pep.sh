build_ios() {
  echo -e "\n${YELLOW}в–¶ Compilando IPA iOS...${NC}"
  cd "$ROOT/mobile_flutter" && flutter build ipa --release
  echo -e "\n${GREEN}вњ… IPA listo en:${NC}"
  echo "   $ROOT/mobile_flutter/build/ios/ipa/"
}

