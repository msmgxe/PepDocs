}

# в”Ђв”Ђ 6 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
push_github() {
  cd "$ROOT"
  echo ""
  git status
  echo ""
  read -p "  Mensaje de commit: " msg
  if [ -z "$msg" ]; then
    echo -e "${RED}вќЊ Mensaje vacГ­o вЂ” cancelado.${NC}"
    return 1
  fi
  git add .
  git commit -m "$msg"
  git push
  echo -e "\n${GREEN}вњ… Cambios subidos a GitHub.${NC}"
}

